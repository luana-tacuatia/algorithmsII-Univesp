class Employee:

    def get_name(self):
        return self.name

    def set_name(self, name):
        self.name = name

    def get_admission_date(self):
        return self.admission_date

    def set_admission_date(self, admission_date):
        self.admission_date = admission_date

    def get_salary(self):
        return self.salary

    def set_salary(self, salary):
        self.salary = salary


